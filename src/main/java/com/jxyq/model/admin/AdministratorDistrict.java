package com.jxyq.model.admin;

public class AdministratorDistrict {
    private int administrator_district_id;
    private int administrator_id;
    private int district_id;
    private int province_id;
    private int city_id;
    private int zone_id;

    public int getAdministrator_district_id() {
        return administrator_district_id;
    }

    public void setAdministrator_district_id(int administrator_district_id) {
        this.administrator_district_id = administrator_district_id;
    }

    public int getAdministrator_id() {
        return administrator_id;
    }

    public void setAdministrator_id(int administrator_id) {
        this.administrator_id = administrator_id;
    }

    public int getDistrict_id() {
        return district_id;
    }

    public void setDistrict_id(int district_id) {
        this.district_id = district_id;
    }

    public int getProvince_id() {
        return province_id;
    }

    public void setProvince_id(int province_id) {
        this.province_id = province_id;
    }

    public int getCity_id() {
        return city_id;
    }

    public void setCity_id(int city_id) {
        this.city_id = city_id;
    }

    public int getZone_id() {
        return zone_id;
    }

    public void setZone_id(int zone_id) {
        this.zone_id = zone_id;
    }
}
